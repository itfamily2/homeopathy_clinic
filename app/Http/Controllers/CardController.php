    {
        $request -> validate([
            'patientID' => 'required',
            'orderID' => 'required',
            'cardNum' => 'required',
            'bank' => 'required',
            'serialNum' => 'required',
            'cardType' => 'required'
            
        ]);

        card::create($request->all());

        return view('payment.paymentConfirm');
    }

